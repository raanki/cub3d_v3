int	ft_atoi(const char *s)
{
	int				sign;
	int				n;
	int				i;
	unsigned int	test;

	n = 0;
	sign = 1;
	i = 0;
	while (s[i] == '\f' || s[i] == ' ' || s[i] == '\n'
		|| s[i] == '\t' || s[i] == '\v' || s[i] == '\r' )
		i++;
	if (s[i] == '-')
	{
		sign = -1;
		i++;
	}
	else if (s[i] == '+')
		i++;
	while (s[i] != 0 && s[i] >= '0' && s[i] <= '9')
	{
		test = 10 * n + (s[i] - '0');
		if ((test <= 2147483647 && sign == 1)
			|| (test <= 2147483648 && sign == -1))
		n = 10 * n + (s[i] - '0');
		i++;
	}
	return (n * sign);
}